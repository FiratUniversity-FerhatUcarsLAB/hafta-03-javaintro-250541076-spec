public class DikdörtgenHesap {
    public static void main(String[] args) {
    double en = 4.5;
    double boy = 7.9;
    
    // Alan hesapla
    double alan = en * boy;
    
    // Cevre hesapla
    double cevre = (en + boy) * 2;
    
    // Sonucları hesapla
    System.out.println("Alan: " + alan);
    System.out.println("Cevre: " + cevre);
    
    }
}
